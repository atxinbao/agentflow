use agentflow_action_contract::ActionContractRegistry;
use agentflow_ontology::OntologyRegistry;

use crate::model::{
    ObjectStateDefinition, ObjectStateMachine, ObjectStateMachineBundle, ObjectStateMachineStatus,
    StateProjectionHints, StateTransitionDefinition, OBJECT_STATE_BUNDLE_VERSION,
};
use crate::registry::ObjectStateMachineRegistry;
use crate::report::ObjectStateValidationReport;

pub const CORE_OBJECT_STATE_ID: &str = "agentflow.object-state.core";
pub const CORE_OBJECT_STATE_NAMESPACE: &str = "agentflow.object-state.core";
pub const CORE_OBJECT_STATE_VERSION: &str = "v1-draft";
pub const CORE_OBJECT_STATE_REF: &str = "agentflow.object-state.core@v1-draft";

pub fn core_object_state_bundle() -> ObjectStateMachineBundle {
    ObjectStateMachineBundle {
        version: OBJECT_STATE_BUNDLE_VERSION.into(),
        registry_id: CORE_OBJECT_STATE_ID.into(),
        namespace: CORE_OBJECT_STATE_NAMESPACE.into(),
        definition_version: CORE_OBJECT_STATE_VERSION.into(),
        status: ObjectStateMachineStatus::Draft,
        state_machines: vec![
            requirement_state_machine(),
            spec_state_machine(),
            issue_state_machine(),
            run_state_machine(),
            audit_state_machine(),
            finding_state_machine(),
        ],
    }
}

pub fn core_object_state_registry(
    ontology_registry: &OntologyRegistry,
    action_registry: &ActionContractRegistry,
) -> Result<ObjectStateMachineRegistry, ObjectStateValidationReport> {
    ObjectStateMachineRegistry::load_bundle(
        core_object_state_bundle(),
        ontology_registry,
        action_registry,
    )
}

