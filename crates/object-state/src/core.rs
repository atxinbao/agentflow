fn machine(
    state_machine_id: &str,
    object_type: &str,
    initial_state: &str,
    states: Vec<ObjectStateDefinition>,
    terminal_states: Vec<&str>,
    transitions: Vec<StateTransitionDefinition>,
    invariants: Vec<&str>,
    projection_hints: StateProjectionHints,
) -> ObjectStateMachine {
    ObjectStateMachine {
        state_machine_id: state_machine_id.into(),
        object_type: object_type.into(),
        namespace: CORE_OBJECT_STATE_NAMESPACE.into(),
        version: CORE_OBJECT_STATE_VERSION.into(),
        status: ObjectStateMachineStatus::Draft,
        initial_state: initial_state.into(),
        states,
        terminal_states: terminal_states.into_iter().map(str::to_string).collect(),
        transitions,
        invariants: invariants.into_iter().map(str::to_string).collect(),
        projection_hints,
    }
}

fn state(
    state_id: &str,
    name: &str,
    description: &str,
    is_terminal: bool,
    is_error_state: bool,
    legacy_status_aliases: Vec<&str>,
) -> ObjectStateDefinition {
    ObjectStateDefinition {
        state_id: state_id.into(),
        name: name.into(),
        description: description.into(),
        is_terminal,
        is_error_state,
        legacy_status_aliases: legacy_status_aliases
            .into_iter()
            .map(str::to_string)
            .collect(),
    }
}

#[allow(clippy::too_many_arguments)]
fn transition(
    transition_id: &str,
    from_states: Vec<&str>,
    to_state: &str,
    action_type: &str,
    compatibility_action_aliases: Vec<&str>,
    guards: Vec<&str>,
    required_evidence: Vec<&str>,
    emitted_events: Vec<&str>,
    link_effects: Vec<&str>,
    role_hints: Vec<&str>,
) -> StateTransitionDefinition {
    StateTransitionDefinition {
        transition_id: transition_id.into(),
        from_states: from_states.into_iter().map(str::to_string).collect(),
        to_state: to_state.into(),
        action_type: action_type.into(),
        compatibility_action_aliases: compatibility_action_aliases
            .into_iter()
            .map(str::to_string)
            .collect(),
        guards: guards.into_iter().map(str::to_string).collect(),
        required_evidence: required_evidence.into_iter().map(str::to_string).collect(),
        emitted_events: emitted_events.into_iter().map(str::to_string).collect(),
        link_effects: link_effects.into_iter().map(str::to_string).collect(),
        role_hints: role_hints.into_iter().map(str::to_string).collect(),
    }
}

fn hints(timeline_order: Vec<&str>, preferred_current_states: Vec<&str>) -> StateProjectionHints {
    StateProjectionHints {
        timeline_order: timeline_order.into_iter().map(str::to_string).collect(),
        preferred_current_states: preferred_current_states
            .into_iter()
            .map(str::to_string)
            .collect(),
    }
}
